void Eliana(void)
{
	// Init BIGARRAY/LCD str
	USART_LOGIC_UNK5 = 0x00;
	do{
		BIGARRAY[USART_LOGIC_UNK5] = 0x20; // init with spaces
	} while (USART_LOGIC_UNK5 < 0xa);

	MLOGIC_SUB2_UNK1 = 0x10;
	MLOGIC_SUB2_UNK2 = 0x27;
	EVELISE_UNK1 = MLOGIC_SUB2_UNK5;
	ENTRY_SUB2_UNK1 = MLOGIC_SUB2_UNK4;
	Elise(MLOGIC_SUB2_UNK4);

	ENTRY_SUB2_UNK1 = ESTELLE_UNK1;
	EVELISE_UNK1 = ESTELLE_UNK2;
	MLOGIC_SUB2_UNK1 = 0x0a;
	MLOGIC_SUB2_UNK2 = 0x00;
	Elise(0x00);

	BIGARRAY[0] = ENTRY_SUB2_UNK1 | 0x30;
	MLOGIC_SUB2_UNK1 = 0xe8;
	MLOGIC_SUB2_UNK2 = 0x03;
	EVELISE_UNK1 = MLOGIC_SUB2_UNK5;
	ENTRU_SUB2_UNK1 = MLOGIC_SUB2_UNK4;
	Elise(MLOGIC_SUB2_UNK4);

	ENTRY_SUB2_UNK1 = ESTELLE_UNK1;
	EVELISE_UNK1 = ESTELLE_UNK2;
	MLOGIC_SUB2_UNK1 = 0xa;
	MLOGIC_SUB2_UNK2 = 0x0;
	Elise(0x0);

	BIGARRAY[1] = ENTRY_SUB2_UNK1 | 0x30;
	MLOGIC_SUB2_UNK1 = 0x64;
	MLOGIC_SUB2_UNK2 = 0x0;
	EVELISE_UNK1 = MLOGIC_SUB_UNK5;
	ENTRY_SUB2_UNK1 = MLOGIC_SUB_UNK4;
	Elise(MLOGIC_SUB_UNK4);

	ENTRY_SUB2_UNK1 = ESTELLE_UNK1;
	EVELISE_UNK1 = ESTELLE_UNK2;
	MLOGIC_SUB2_UNK1 = 0xa;
	MLOGIC_SUB2_UNK2 = 0x0;
	Elise(0x0);

	BIGARRAY[2] = ENTRY_SUB2_UNK1 | 0x30;
	MLOGIC_SUB2_UNK1 = 0xa;
	MLOGIC_SUB2_UNK2 = 0x0;
	EVELISE_UNK1 = MLOGIC_SUB_UNK5;
	ENTRY_SUB2_UNK1 = MLOGIC_SUB_UNK4;
	Elise(MLOGIC_SUB_UNK4);

	ENTRY_SUB2_UNK1 = ESTELLE_UNK1;
	EVELISE_UNK1 = ESTELLE_UNK2;
	MLOGIC_SUB2_UNK1 = 0xa;
	MLOGIC_SUB2_UNK2 = 0x0;
	Elise(0x0);

	BIGARRAY[3] = ENTRY_SUB2_UNK1 | 0x30;
	MLOGIC_SUB2_UNK1 = 0xa;
	MLOGIC_SUB2_UNK2 = 0x0;
	EVELISE_UNK1 = MLOGIC_SUB_UNK5;
	ENTRY_SUB2_UNK1 = MLOGIC_SUB_UNK4;
	Elise(MLOGIC_SUB_UNK4);

	BIGARRAY[4] = ENTRY_SUB2_UNK1 | 0x30;

	// No idea what this is for
	if (BIGARRAY[0] == 0x30)
		BIGARRAY[0] = 0x20;
	else if (BIGARRAY[1] == 0x30)
		BIGARRAY[1] = 0x20;
	else if (BIGARRAY[2] == 0x30)
		BIGARRAY[2] = 0x20;
	else if (BIGARRAY[3] == 0x30)
		BIGARRAY[3] = 0x30;
}
